            <h3>About the Program</h3>

            <p align="justify">
                Digital forensic practices stem from forensic science, the science of collecting and examining evidence
                or materials. Digital or computer forensics focuses on the digital domain including computer forensics,
                network forensics, and mobile forensics. As the cyber security profession evolves, organizations are
                learning the importance of employing digital forensic practices into their everyday activities. Computer
                forensic practices can help investigate attacks, system anomalies, or even help System administrators
                detect a problem by defining what is normal functional specifications and validating system information
                for irregular behaviors.</p>

            <p align="justify">
                In the event of a cyber-attack or incident, it is critical investigations be carried out in a manner
                that is forensically sound to preserve evidence in the event of a breach of the law. Far too many
                cyber-attacks are occurring across the globe where laws are clearly broken and due to improper or
                non-existent forensic investigations, the cyber criminals go either unidentified, undetected, or are
                simply not prosecuted.
            </p>

            <p align="justify"> Cyber Security professionals who acquire a firm grasp on the principles of digital
                forensics can become invaluable members of Incident Handling and Incident response teams. The Computer
                Forensic course provides a strong baseline knowledge of key concepts and practices in the digital
                forensic domains relevant to today’s organizations. CHFI provides its attendees a firm grasp on the
                domains of digital forensics.
            </p>
