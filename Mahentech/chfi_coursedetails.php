            <h3>About the Exam</h3>
            <p align="justify">The CHFI certification is awarded after successfully passing the exam EC0 312-49. CHFI
                EC0 312-49 exams are available at ECC exam center around the world.</p>
            <h3>CHFI Exam Details</h3>
            <ol>
                <li>Number of Questions: 150</li>
                <li>Test Duration: 4 hours</li>
                <li>Test Format: Multiple choice</li>
                <li>Test Delivery: ECC exam portal</li>
            </ol>
            <br>
            <h3>Passing Score</h3>
            <p align="justify">
                In order to maintain the high integrity of our certifications exams, EC-Council Exams are provided in
                multiple forms (I.e. different question banks). Each form is carefully analyzed through beta testing
                with an appropriate sample group under the purview of a committee of subject matter experts that ensure
                that each of our exams not only has academic rigor but also has “real world” applicability. We also have
                a process to determine the difficulty rating of each question. The individual rating then contributes to
                an overall “Cut Score” for each exam form. To ensure each form has equal assessment standards, cut
                scores are set on a “per exam form” basis. Depending on which exam form is challenged, cut scores can
                range from 60% to 78%.
            </p>
